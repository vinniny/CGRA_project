#!/usr/bin/env python3
"""
quantize_to_int32.py — Convert float checkpoint to GoldenWeights binary.

Reads vn_char_best.pt (PyTorch checkpoint) and emits:
  - golden_weights.bin:    Raw GoldenWeights struct (99,192 bytes = 24,798 float32)
  - golden_weights_scale.h: Placeholder header (scales all 1.0; weights stored as float)

GoldenWeights struct layout (must match lpr_golden.h exactly):
  conv1_w  [8][3][3][1]   = 72   float32
  conv1_b  [8]            = 8    float32
  conv2_w  [16][3][3][8]  = 1152 float32
  conv2_b  [16]           = 16   float32
  fc_w     [784][30]      = 23520 float32
  fc_b     [30]           = 30   float32
  Total = 24798 float32 = 99192 bytes

Weights are stored as raw IEEE 754 float32 (little-endian), which the
ARM Cortex-A9 VFP reads natively via fread() into a float[] struct.
sizeof(float)==sizeof(int32_t)==4, so the file size is unchanged from
the previous int32 layout.
"""

import argparse
import numpy as np
import torch
import torch.nn as nn
from pathlib import Path

# Must match lpr_golden.h VN_CHAR_MAP
VN_CHAR_MAP = ['1','2','3','4','5','6','7','8','9',
               'A','B','C','D','E','F','G','H','K','L','M',
               'N','P','S','T','U','V','X','Y','Z','0']

# Golden network — must mirror train_vn_char.py
class VNCharNet(nn.Module):
    def __init__(self, num_classes=30):
        super().__init__()
        self.conv1 = nn.Conv2d(1, 8,  3, padding=1, bias=True)
        self.conv2 = nn.Conv2d(8, 16, 3, padding=1, bias=True)
        self.pool  = nn.MaxPool2d(2, 2)
        self.relu  = nn.ReLU(inplace=True)
        self.fc    = nn.Linear(16 * 7 * 7, num_classes, bias=True)

    def forward(self, x):
        x = self.pool(self.relu(self.conv1(x)))
        x = self.pool(self.relu(self.conv2(x)))
        x = x.view(x.size(0), -1)
        return self.fc(x)


def pack_weights_float(model):
    """
    Extract float32 weights in GoldenWeights field order.
    Weight reshaping:
      conv1.weight: PyTorch [C_out=8, C_in=1, KH=3, KW=3]
                    -> golden [C_out][KH][KW][C_in] = [8][3][3][1]
      conv2.weight: PyTorch [C_out=16, C_in=8, KH=3, KW=3]
                    -> golden [C_out][KH][KW][C_in] = [16][3][3][8]
      fc.weight:    PyTorch [out=30, in=784] -> golden [784][30] (transpose)
    """
    def info(name, arr):
        print(f"[quantize]   {name:15s}: shape={arr.shape}  "
              f"max_abs={np.max(np.abs(arr)):.4f}  "
              f"range=[{arr.min():.4f}, {arr.max():.4f}]")

    conv1_w = model.conv1.weight.detach().numpy().transpose(0, 2, 3, 1)  # [8,3,3,1]
    info('conv1_w', conv1_w)

    conv1_b = model.conv1.bias.detach().numpy()  # [8]
    info('conv1_b', conv1_b)

    conv2_w = model.conv2.weight.detach().numpy().transpose(0, 2, 3, 1)  # [16,3,3,8]
    info('conv2_w', conv2_w)

    conv2_b = model.conv2.bias.detach().numpy()  # [16]
    info('conv2_b', conv2_b)

    # PyTorch fc.weight [30,784] is CHW-indexed (pool2 CHW [16,7,7] → flat).
    # C golden model flattens pool2 in HWC order ([7,7,16] → flat).
    # Permute rows: CHW index c*49+h*7+w → HWC index h*112+w*16+c.
    fc_w_chw = model.fc.weight.detach().numpy().T      # [784, 30] CHW-indexed
    fc_w = fc_w_chw.reshape(16, 7, 7, 30).transpose(1, 2, 0, 3).reshape(784, 30)  # HWC-indexed
    info('fc_w', fc_w)

    fc_b = model.fc.bias.detach().numpy()  # [30]
    info('fc_b', fc_b)

    return {
        'conv1_w': conv1_w.astype(np.float32),
        'conv1_b': conv1_b.astype(np.float32),
        'conv2_w': conv2_w.astype(np.float32),
        'conv2_b': conv2_b.astype(np.float32),
        'fc_w':    fc_w.astype(np.float32),
        'fc_b':    fc_b.astype(np.float32),
    }


def write_golden_weights_bin(weights, out_path):
    """Write GoldenWeights struct as little-endian float32 binary."""
    expected_counts = {
        'conv1_w': 8 * 3 * 3 * 1,
        'conv1_b': 8,
        'conv2_w': 16 * 3 * 3 * 8,
        'conv2_b': 16,
        'fc_w':    784 * 30,
        'fc_b':    30,
    }
    total = sum(expected_counts.values())
    assert total == 24798, f"Expected 24798 floats, got {total}"

    with open(out_path, 'wb') as f:
        for field, expected in expected_counts.items():
            arr = weights[field].flatten().astype('<f4')
            assert len(arr) == expected, \
                f"{field}: expected {expected} elements, got {len(arr)}"
            f.write(arr.tobytes())

    size = Path(out_path).stat().st_size
    assert size == 24798 * 4, f"File size mismatch: {size} != {24798*4}"
    print(f"[quantize] Written {out_path}  ({size} bytes = {size//4} float32)")


def write_scale_header(out_path):
    """Write C header noting that weights are stored as raw float32."""
    lines = [
        "/* golden_weights_scale.h — auto-generated by quantize_to_int32.py */",
        "/* Weights stored as raw float32; scale = 1.0 (no quantization). */",
        "#pragma once",
        "",
        "#define GOLDEN_WEIGHTS_ARE_FLOAT32   1",
        "",
    ]
    with open(out_path, 'w') as f:
        f.write('\n'.join(lines))
    print(f"[quantize] Written {out_path}")


def verify_inference(model, weights, data_dir, n_samples=200):
    """Quick sanity check: compare PyTorch float predictions vs binary weights."""
    try:
        d = torch.load(Path(data_dir) / 'test.pt', weights_only=True)
        X = d['X'][:n_samples]
        y = d['y'][:n_samples]
    except Exception:
        print("[quantize] WARN: Could not load test split for verification — skipping.")
        return

    model.eval()
    with torch.no_grad():
        # PyTorch model expects [0,255] float inputs (same as training)
        logits = model(X)
        preds = logits.argmax(1).numpy()

    correct = (preds == y.numpy()).sum()
    acc = correct / len(y)
    print(f"[quantize] Float model sanity check: {correct}/{len(y)} = {acc:.2%} "
          f"(on first {n_samples} test images)")
    if acc < 0.89:
        print("[quantize] WARNING: float model accuracy < 89% — check checkpoint.")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--checkpoint', default='vn_char_best.pt')
    parser.add_argument('--out_bin',    default='golden_weights.bin')
    parser.add_argument('--out_scale',  default='golden_weights_scale.h')
    parser.add_argument('--data_dir',   default='datasets/processed',
                        help='Processed data dir for sanity-check inference')
    parser.add_argument('--skip_verify', action='store_true',
                        help='Skip float-model accuracy verification')
    args = parser.parse_args()

    expected_bytes = 24798 * 4
    print(f"[quantize] Expected GoldenWeights size: {expected_bytes} bytes ({24798} float32)")

    ckpt = torch.load(args.checkpoint, map_location='cpu', weights_only=True)
    model = VNCharNet(num_classes=30)
    model.load_state_dict(ckpt['model_state'])
    model.eval()
    print(f"[quantize] Loaded checkpoint: {args.checkpoint}")
    print(f"[quantize]   val_acc={ckpt.get('val_acc', 0.0):.2%}  epoch={ckpt.get('epoch', '?')}")

    if not args.skip_verify:
        verify_inference(model, None, args.data_dir)

    print(f"[quantize] Extracting float32 weights...")
    weights = pack_weights_float(model)

    write_golden_weights_bin(weights, args.out_bin)
    write_scale_header(args.out_scale)

    print(f"\n[quantize] Done.")
    print(f"  Load with: golden_weights_load(&w, \"{args.out_bin}\");")


if __name__ == '__main__':
    main()
