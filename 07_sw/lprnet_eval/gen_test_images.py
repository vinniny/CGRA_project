#!/usr/bin/env python3
"""
gen_test_images.py — Export N-per-class test images from the VN font dataset
                     for embedding in demo_lpr.c.

Outputs:
  demo_lpr_test_images.bin  — raw int32 pixel data (N_images × 784 × 4 bytes)
  demo_lpr_test_images.h    — ground-truth labels + class indices (no pixel data)

The .bin file is embedded via .incbin into the .lpr_images DDR section so it
does not consume OCM space.  The .h file stays in OCM rodata (≤ a few KB).

Usage (from 07_sw/lprnet_eval/):
  python3 gen_test_images.py [--n_per_class 2] [--out_dir ../baremetal]
"""

import argparse
import struct
import numpy as np
import torch
from pathlib import Path

# Must match lpr_golden.h VN_CHAR_MAP
VN_CHAR_MAP = ['1','2','3','4','5','6','7','8','9',
               'A','B','C','D','E','F','G','H','K','L','M',
               'N','P','S','T','U','V','X','Y','Z','0']
CHAR_TO_IDX = {c: i for i, c in enumerate(VN_CHAR_MAP)}
assert len(VN_CHAR_MAP) == 30


def load_test_split(data_dir):
    path = Path(data_dir) / 'test.pt'
    d = torch.load(path, weights_only=True)
    return d['X'].numpy(), d['y'].numpy()


def pick_images(X, y, n_per_class):
    """
    Return n_per_class representative uint8 [28,28] images per VN class,
    ordered by VN_CHAR_MAP index then by sample index within the class.
    Picks samples evenly distributed across each class's test pool.
    """
    results = []  # list of (char, class_idx, img_uint8)

    for cls_idx, ch in enumerate(VN_CHAR_MAP):
        candidates = np.where(y == cls_idx)[0]
        if len(candidates) == 0:
            raise ValueError(f"No test images for class '{ch}' (idx {cls_idx})")

        # Evenly spaced picks across the candidate pool
        n = min(n_per_class, len(candidates))
        if n < n_per_class < 9999:
            print(f"[gen] WARNING: class '{ch}' has only {len(candidates)} samples "
                  f"(requested {n_per_class})")
        picks = [candidates[int(len(candidates) * (k + 0.5) / n)] for k in range(n)]

        for sel in picks:
            img_f = X[sel, 0]                              # [28,28] float32 [0,255]
            img_u8 = img_f.round().clip(0, 255).astype(np.uint8)
            results.append((ch, cls_idx, img_u8))

    return results


def write_bin(results, out_path):
    """Write all images as a flat int32 binary: each image is 784 int32 values."""
    with open(out_path, 'wb') as f:
        for _, _, img_u8 in results:
            pixels = img_u8.flatten().astype(np.int32)
            f.write(struct.pack(f'<{len(pixels)}i', *pixels))
    n = len(results)
    size = n * 784 * 4
    print(f"[gen] Written {out_path}  ({n} images × 784 int32 = {size} bytes)")


def write_header(results, out_path):
    """Write C header with ground-truth labels and class indices (no pixel data)."""
    n = len(results)
    chars      = [ch  for ch, _, _ in results]
    class_idxs = [ci  for _, ci, _ in results]

    lines = [
        f"/* demo_lpr_test_images.h — auto-generated by gen_test_images.py",
        f" * Pixel data is in demo_lpr_test_images.bin → .lpr_images DDR section.",
        f" * Access image i via: lpr_images_start + (uint32_t)i * LPR_TEST_IMG_PIXELS */",
        "#pragma once",
        "#include <stdint.h>",
        f"#define LPR_TEST_NUM_IMAGES  {n}",
        "#define LPR_TEST_IMG_PIXELS  784   /* 28×28 int32 pixels per image */",
        "",
        f"static const char    lpr_test_chars[{n}] = {{",
        "    " + ", ".join(f"'{c}'" for c in chars),
        "};",
        "",
        f"static const uint8_t lpr_test_class_idx[{n}] = {{",
        "    " + ", ".join(str(ci) for ci in class_idxs),
        "};",
        "",
    ]

    with open(out_path, 'w') as f:
        f.write('\n'.join(lines))
    print(f"[gen] Written {out_path}  ({n} labels + class indices)")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--data_dir',    default='datasets/processed')
    parser.add_argument('--n_per_class', type=int, default=9999,
                        help='Images per class (default 9999 = all available)')
    parser.add_argument('--out_dir',     default='../baremetal')
    args = parser.parse_args()

    out_dir = Path(args.out_dir)

    print(f"[gen] Loading test split: {args.data_dir}/test.pt")
    X, y = load_test_split(args.data_dir)
    cap = f"up to {args.n_per_class}" if args.n_per_class < 9999 else "all available"
    print(f"[gen] Test set: {len(X)} images, {cap} picks/class")

    results = pick_images(X, y, args.n_per_class)

    # Accuracy check: verify the golden Python model would classify these correctly
    # (quick sanity — not a full eval, just checks the picked images are sane)
    ok = sum(1 for ch, ci, img in results
             if img.min() < 200)   # at least some dark pixels → not blank
    print(f"[gen] {ok}/{len(results)} images have visible strokes (min pixel < 200)")

    write_bin(results, out_dir / 'demo_lpr_test_images.bin')
    write_header(results, out_dir / 'demo_lpr_test_images.h')

    # Summary
    print(f"[gen] Classes covered: {len(set(ci for _, ci, _ in results))}/30")
    print(f"[gen] Total size: {len(results) * 784 * 4} bytes in .bin  "
          f"+ small .h header")


if __name__ == '__main__':
    main()
