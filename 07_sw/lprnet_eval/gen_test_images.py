#!/usr/bin/env python3
"""
gen_test_images.py — Export 7 test character images from the VN font dataset
                     as a C header for embedding in demo_lpr.c.

Usage (from 07_sw/lprnet_eval/):
  python3 gen_test_images.py [--out ../baremetal/demo_lpr_test_images.h]

Picks one image per target character from datasets/processed/test.pt and
writes it as a const int32_t[784] array.  The 7 characters represent a
typical Vietnamese plate sequence that exercises both digits and letters.
"""

import argparse
import numpy as np
import torch
from pathlib import Path

# Must match lpr_golden.h VN_CHAR_MAP
VN_CHAR_MAP = ['1','2','3','4','5','6','7','8','9',
               'A','B','C','D','E','F','G','H','K','L','M',
               'N','P','S','T','U','V','X','Y','Z','0']
CHAR_TO_IDX = {c: i for i, c in enumerate(VN_CHAR_MAP)}

# 7 plate characters: '5','1','K','2','3','4','A'
# Reads as plate "51K234A" — exercises 4 digits + 3 letters
TARGET_CHARS = ['5', '1', 'K', '2', '3', '4', 'A']


def load_test_split(data_dir):
    path = Path(data_dir) / 'test.pt'
    d = torch.load(path, weights_only=True)
    return d['X'].numpy(), d['y'].numpy()


def pick_images(X, y, target_chars):
    """Return one representative uint8 [28,28] image per target character."""
    images = {}
    for ch in target_chars:
        idx = CHAR_TO_IDX[ch]
        mask = (y == idx)
        candidates = np.where(mask)[0]
        if len(candidates) == 0:
            raise ValueError(f"No test images found for class '{ch}' (idx {idx})")
        # Pick the middle candidate for a typical-looking sample
        sel = candidates[len(candidates) // 2]
        img_f = X[sel, 0]                                  # [28,28] float32 [0,255]
        img_u8 = img_f.round().clip(0, 255).astype(np.uint8)
        images[ch] = img_u8
    return images


def write_header(images, target_chars, out_path):
    lines = [
        "/* demo_lpr_test_images.h — auto-generated by gen_test_images.py",
        " * 7 × 28×28 = 784 int32 pixel values per image, range [0,255].",
        " * Matches the golden_infer() input format (int32_t[784]).         */",
        "#pragma once",
        "#include <stdint.h>",
        f"#define LPR_TEST_NUM_IMAGES {len(target_chars)}",
        "",
    ]

    # Character label array
    char_list = ', '.join(f"'{c}'" for c in target_chars)
    lines.append(f"static const char lpr_test_chars[{len(target_chars)}] = {{{char_list}}};")
    lines.append("")

    var_names = []
    for i, ch in enumerate(target_chars):
        img = images[ch].flatten().astype(np.int32)
        var = f"lpr_test_img_{i}"
        var_names.append(var)

        # Format as C array — 16 values per row for readability
        vals = []
        for j in range(0, len(img), 16):
            row = img[j:j+16]
            vals.append("    " + ", ".join(str(v) for v in row))
        body = ",\n".join(vals)

        lines.append(f"/* [{i}] '{ch}' (class index {CHAR_TO_IDX[ch]}) */")
        lines.append(f"static const int32_t {var}[784] = {{")
        lines.append(body)
        lines.append("};")
        lines.append("")

    # Pointer array
    ptrs = ", ".join(var_names)
    lines.append(f"static const int32_t *const lpr_test_images[{len(target_chars)}] = {{")
    lines.append(f"    {ptrs}")
    lines.append("};")
    lines.append("")

    with open(out_path, 'w') as f:
        f.write('\n'.join(lines))
    print(f"[gen] Written {out_path}  ({len(target_chars)} images × 784 int32)")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--data_dir', default='datasets/processed')
    parser.add_argument('--out',      default='../baremetal/demo_lpr_test_images.h')
    args = parser.parse_args()

    print(f"[gen] Loading test split: {args.data_dir}/test.pt")
    X, y = load_test_split(args.data_dir)
    print(f"[gen] Test set: {len(X)} images")

    images = pick_images(X, y, TARGET_CHARS)
    for ch, img in images.items():
        print(f"[gen]   '{ch}' → shape={img.shape}  min={img.min()}  max={img.max()}")

    write_header(images, TARGET_CHARS, args.out)


if __name__ == '__main__':
    main()
